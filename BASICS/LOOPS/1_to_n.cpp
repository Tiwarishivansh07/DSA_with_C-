/*
   here in this program we are using a for loop to print the number from 1 to n(user input)
*/
#include<iostream>
using namespace std;
int main()
{
    int n;
    cout<<"enter number:";
    cin>>n;
    for(int i=1;i<=n;i++)
    {
        cout<<i<<" ";
    }
    cout<<endl;
    cout<<"BAS MUNNA ITNA HI HAI.";
    return 0;
}